export const connectSandbox = async (
  orgId: string,
  projectId: string,
  sandboxId: string
) => {
  return await post<TSandboxConnectResult>(
    `/orgs/${orgId}/projects/${projectId}/sandboxes/${sandboxId}/connect`,
    {}
  )
}

