/**
 * Full pod lifecycle setup: create project + create sandbox config + start pod + wait for Running.
 * Returns IDs needed for tests and cleanup.
 */
export const setupRunningPod = async (
  orgId: string,
  configOverrides?: Record<string, unknown>
): Promise<TSandboxSetup> => {
  const projRes = await post<Record<string, any>>(
    `/orgs/${orgId}/projects`,
    { name: uniqueName('sb-test-project'), orgId }
  )
  if (!projRes.ok) throw new Error(`Failed to create project: HTTP ${projRes.status}`)
  const projectId = projRes.data.id

  const sandboxName = uniqueName('test-sandbox')
  const sbRes = await post<Record<string, any>>(
    `/orgs/${orgId}/sandboxes`,
    {
      name: sandboxName,
      config: { ...defaultSandboxConfig, ...configOverrides },
      orgId,
      projectId,
    }
  )
  if (!sbRes.ok) throw new Error(`Failed to create sandbox config: HTTP ${sbRes.status}`)
  const sandboxId = sbRes.data.id

  let startRes: Awaited<ReturnType<typeof post<{ podName: string }>>>
  for (let attempt = 0; attempt < 3; attempt++) {
    startRes = await post<{ podName: string }>(
      `/orgs/${orgId}/projects/${projectId}/sandboxes/${sandboxId}/start`,
      {}
    )
    if (startRes.ok) break
    if (attempt < 2) await new Promise(r => setTimeout(r, 3_000 * (attempt + 1)))
  }
  if (!startRes!.ok) throw new Error(`Failed to start pod: HTTP ${startRes!.status}`)
  const podName = startRes!.data.podName

  await waitForPodState(orgId, projectId, sandboxId, podName, 'Running', 90_000)

  return { podName, projectId, sandboxId, sandboxName }
}

/**
 * Best-effort cleanup: stop pod + delete sandbox config + delete project.
 * All errors are swallowed — cleanup is best-effort.
 */
export const cleanupSandbox = async (
  orgId: string,
  setup: Partial<TSandboxSetup>
) => {
  if (setup.podName && setup.sandboxId && setup.projectId) {
    try {
      await api(`/orgs/${orgId}/projects/${setup.projectId}/sandboxes/${setup.sandboxId}/stop`, {
        method: 'DELETE',
        body: { podName: setup.podName },
      })
    } catch { /* best-effort */ }
  }
  if (setup.sandboxId) await tryDelete(`/orgs/${orgId}/sandboxes/${setup.sandboxId}`)
  if (setup.projectId) await tryDelete(`/orgs/${orgId}/projects/${setup.projectId}`)
}
